public class ComputerUsingOverloadedMethods {

    public void add(int x, int y) {
        System.out.println(x + y);
    }

    public void add(long x, long y) {
        System.out.println(x + y);
    }

    public void subtract(int x, int y) {
        System.out.println(x - y);
    }

    public void subtract(long x, long y) {
        System.out.println(x - y);
    }

}
